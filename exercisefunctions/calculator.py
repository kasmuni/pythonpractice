# Creating a calculator class

class calculator():

    def __init__(self):
        pass

    def add(self,a, b):
        result = a + b
        return result

    def subtract(self, a, b):
        result = a - b
        return result

    def divide(self, a, b):
        result = a / b
        return result

    def multiply(self, a, b):
        result = a * b
        return result
